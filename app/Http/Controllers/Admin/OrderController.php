    public function index()
    {
        $chartproducts = DB::select('select td.qty,td.price,td.total,p.kdproduk,p.nama,tm.kode AS kdtrans,tm.id,z.name from transaksidetails td 
                LEFT JOIN produks p ON td.idproduk=p.id 
                LEFT JOIN transaksimasters tm ON td.idmaster=tm.id 
                LEFT JOIN (SELECT v.id,v.iduser,u.name FROM users u, transaksimasters v WHERE u.id=v.iduser) z ON td.idmaster=z.id
                 ORDER BY tm.tanggal');
        return view('admin.orders.index',['chartproducts'=>$chartproducts])
        ->with('i',(request()->input('page', 1) - 1) * 10);
    }

